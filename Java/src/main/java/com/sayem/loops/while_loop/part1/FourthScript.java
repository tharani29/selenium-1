package com.sayem.loops.while_loop.part1;

public class FourthScript {

    public static void main(String [] args){

        String output = "";
        int x = 0;
        int y = 0;

        while (y <= x){
            while (y <= x){
                y++;
                output += "*";

            }

            x++;
            output += "\n";
        }
    }
}
