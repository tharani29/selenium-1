package com.sayem.operator.aritmatika;

public class Modulus {
public static void main(String[] args) {
	int a = 5 ;
	int b = 4 ;
	
	double x = 10 ;
	double y = 50 ;
	
	System.out.println("Modulus ... ");
	System.out.println("a % b = " + (a%b));
	System.out.println("a % x = " + (a%x));
	System.out.println("y % b = " + (y%x));
}
}
